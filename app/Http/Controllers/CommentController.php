    {
        // dd($batou);
        $newmessage = New Comment;
        $newmessage->post_id = $batou;


        $newmessage->user_id = User::where('email', $request->email)->first()->id;

        $newmessage->comment = $request->comment;

        $newmessage->save();

        return redirect()->back()->with('success', 'Successfully added comment');
    }

